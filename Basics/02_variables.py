# 02_variables.py

# Variables are used to store information that can be referenced and manipulated in a program.

# This example demonstrates how to use variables and different data types.

def main():
    # Integer variable
    age = 25
    print("Age:", age)

    # Float variable
    height = 5.9
    print("Height:", height)

    # String variable
    name = "Alice"
    print("Name:", name)

    # Boolean variable
    is_student = True
    print("Is student:", is_student)

    # List variable
    hobbies = ["reading", "cycling", "hiking"]
    print("Hobbies:", hobbies)

    # Dictionary variable
    person = {
        "name": "Bob",
        "age": 30,
        "height": 6.0
    }
    print("Person dictionary:", person)

if __name__ == "__main__":
    main()
